package addition;

/**
 * Addition.
 * @author Bianca Kolodziej
 */
public final class Addition {

    /**
     * Privater Konstructor.
     */
    private Addition() { }

    /**
     * Addiert zwei Zahlen.
     * @param i1 erste Zahl
     * @param i2 zweite Zahl
     * @return Summe beider zahlen
     */
    public static int sum(final int i1, final int i2) {
        return i1 + i2;
    }

}
